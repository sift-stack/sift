from __future__ import annotations

import logging
from typing import TYPE_CHECKING, Any, cast

from sift.report_templates.v1.report_templates_pb2 import (
    CreateReportTemplateResponse,
    GetReportTemplateRequest,
    GetReportTemplateResponse,
    ListReportTemplatesRequest,
    ListReportTemplatesResponse,
    UpdateReportTemplateRequest,
    UpdateReportTemplateResponse,
)
from sift.report_templates.v1.report_templates_pb2_grpc import ReportTemplateServiceStub

from sift_client._internal.low_level_wrappers.base import LowLevelClientBase
from sift_client.sift_types.report_template import (
    ReportTemplate,
    ReportTemplateCreate,
    ReportTemplateUpdate,
)
from sift_client.transport import WithGrpcClient

if TYPE_CHECKING:
    from sift_client.transport.grpc_transport import GrpcClient

# Configure logging
logger = logging.getLogger(__name__)


class ReportTemplatesLowLevelClient(LowLevelClientBase, WithGrpcClient):
    """Low-level client for the ReportTemplatesAPI.

    This class provides a thin wrapper around the autogenerated bindings for the ReportTemplateService.
    """

    def __init__(self, grpc_client: GrpcClient):
        """Initialize the ReportTemplatesLowLevelClient.

        Args:
            grpc_client: The gRPC client to use for making API calls.
        """
        super().__init__(grpc_client)

    async def get_report_template(
        self,
        *,
        report_template_id: str | None = None,
        client_key: str | None = None,
        organization_id: str | None = None,
    ) -> ReportTemplate:
        """Get a report template by report_template_id or client_key.

        Args:
            report_template_id: The report template ID to get.
            client_key: The client key of the report template to get.
            organization_id: The organization ID. Only required when getting by
                client_key and the user belongs to multiple organizations.

        Returns:
            The ReportTemplate.

        Raises:
            ValueError: If neither report_template_id nor client_key is provided.
        """
        if not report_template_id and not client_key:
            raise ValueError("Either report_template_id or client_key must be provided")

        request_kwargs: dict[str, Any] = {}
        if report_template_id is not None:
            request_kwargs["report_template_id"] = report_template_id
        if client_key is not None:
            request_kwargs["client_key"] = client_key
        if organization_id is not None:
            request_kwargs["organization_id"] = organization_id

        request = GetReportTemplateRequest(**request_kwargs)
        response = await self._grpc_client.get_stub(ReportTemplateServiceStub).GetReportTemplate(
            request
        )
        grpc_report_template = cast("GetReportTemplateResponse", response).report_template
        return ReportTemplate._from_proto(grpc_report_template)

    async def create_report_template(self, create: ReportTemplateCreate) -> ReportTemplate:
        """Create a report template.

        Args:
            create: The ReportTemplateCreate model with the report template configuration.

        Returns:
            The created ReportTemplate.
        """
        request = create.to_proto()
        response = await self._grpc_client.get_stub(ReportTemplateServiceStub).CreateReportTemplate(
            request
        )
        grpc_report_template = cast("CreateReportTemplateResponse", response).report_template
        return ReportTemplate._from_proto(grpc_report_template)

    async def list_report_templates(
        self,
        *,
        page_size: int | None = None,
        page_token: str | None = None,
        query_filter: str | None = None,
        organization_id: str | None = None,
        order_by: str | None = None,
    ) -> tuple[list[ReportTemplate], str]:
        """List report templates with optional filtering and pagination.

        Args:
            page_size: The maximum number of report templates to return.
            page_token: A page token for pagination.
            query_filter: A CEL filter string.
            organization_id: The organization ID to filter by.
            order_by: How to order the retrieved report templates.

        Returns:
            A tuple of (report_templates, next_page_token).
        """
        request_kwargs: dict[str, Any] = {}
        if page_size is not None:
            request_kwargs["page_size"] = page_size
        if page_token is not None:
            request_kwargs["page_token"] = page_token
        if query_filter is not None:
            request_kwargs["filter"] = query_filter
        if organization_id is not None:
            request_kwargs["organization_id"] = organization_id
        if order_by is not None:
            request_kwargs["order_by"] = order_by

        request = ListReportTemplatesRequest(**request_kwargs)
        response = await self._grpc_client.get_stub(ReportTemplateServiceStub).ListReportTemplates(
            request
        )
        response = cast("ListReportTemplatesResponse", response)
        report_templates = [
            ReportTemplate._from_proto(report_template)
            for report_template in response.report_templates
        ]
        return report_templates, response.next_page_token

    async def list_all_report_templates(
        self,
        *,
        query_filter: str | None = None,
        organization_id: str | None = None,
        order_by: str | None = None,
        page_size: int | None = None,
        max_results: int | None = None,
    ) -> list[ReportTemplate]:
        """List all report templates with optional filtering.

        Args:
            query_filter: A CEL filter string.
            organization_id: The organization ID to filter by.
            order_by: How to order the retrieved report templates.
            page_size: The number of report templates to fetch per request.
            max_results: Maximum number of results to return.

        Returns:
            A list of all matching report templates.
        """
        return await self._handle_pagination(
            self.list_report_templates,
            kwargs={
                "query_filter": query_filter,
                "organization_id": organization_id,
            },
            order_by=order_by,
            max_results=max_results,
            page_size=page_size,
        )

    async def update_report_template(self, update: ReportTemplateUpdate) -> ReportTemplate:
        """Update a report template.

        Args:
            update: The updates to apply.

        Returns:
            The updated report template.
        """
        report_template_proto, field_mask = update.to_proto_with_mask()
        request = UpdateReportTemplateRequest(
            report_template=report_template_proto, update_mask=field_mask
        )
        response = await self._grpc_client.get_stub(ReportTemplateServiceStub).UpdateReportTemplate(
            request
        )
        grpc_report_template = cast("UpdateReportTemplateResponse", response).report_template
        return ReportTemplate._from_proto(grpc_report_template)
